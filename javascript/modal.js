$(document).ready(function() {
  $("#myModal1").on("hidden.bs.modal", function() {
    // to stop the button from being hovered on after the modal is closed. without this, the button will still be hovered on after the modal is closed and so it will appear on the bottom of the card even without being hovered on after the modal is closed.
    $(".project-button").stopImmediatePropagation();
  })
})

